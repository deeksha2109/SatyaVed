export const getRandomMyths = (count = 3) => {
  const shuffled = [...mythsData].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
};

export const getMythById = (id) => {
  return mythsData.find(myth => myth.id === parseInt(id));
};

export const getMythsByCategory = (category) => {
  if (!category) return mythsData;
  return mythsData.filter(myth => myth.category === category);
};

export const getCategories = () => {
  return [...new Set(mythsData.map(myth => myth.category))];
};

export const searchMyths = (searchTerm, category = '') => {
  let filtered = mythsData;
  
  if (category) {
    filtered = filtered.filter(myth => myth.category === category);
  }
  
  if (searchTerm) {
    const term = searchTerm.toLowerCase();
    filtered = filtered.filter(myth => 
      myth.title.toLowerCase().includes(term) ||
      myth.shortDescription.toLowerCase().includes(term) ||
      myth.fullStory.toLowerCase().includes(term)
    );
  }
  
  return filtered;
};
